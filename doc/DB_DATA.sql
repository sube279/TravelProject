\p Creating table PASSENGER
CREATE TABLE PASSENGER (
    ID integer identity PRIMARY KEY ,
    NAME varchar(40) not null,
    FIRSTNAME varchar(40) not null,
	UNIQUE (ID)
);

\p Creating table AIRPORT
CREATE TABLE AIRPORT (
    ID integer identity PRIMARY KEY,
    AIRPORTCODE varchar(10) not null,
    COUNTRY varchar(40) not null,
	LOCATION varchar(40) not null,
	UNIQUE (ID)
);

\p Creating table DEPARTURE
CREATE TABLE DEPARTURE (
    ID integer identity PRIMARY KEY,
    FLIGHTDATE varchar(12) not null,
	DEPARTURETIME varchar(40) not null,
	FK_AIRPORT integer,
	FOREIGN KEY (FK_AIRPORT) REFERENCES AIRPORT,
	UNIQUE (ID)
);

\p Creating table DESTINATION
CREATE TABLE DESTINATION (
    ID integer identity PRIMARY KEY,
    FLIGHTDATE varchar(12) not null,
	DESTINATIONTIME varchar(40) not null,
	FK_AIRPORT integer,
	FOREIGN KEY (FK_AIRPORT) REFERENCES AIRPORT,
	UNIQUE (ID)
);

\p Creating table FLIGHT
CREATE TABLE FLIGHT (
    ID integer identity PRIMARY KEY,
    COMPANY varchar(40) not null,
    FLIGHTNBR varchar(10) not null,
	CAPACITY integer not null,
	FK_DEPARTURE integer,
	FK_DESTINATION integer,
	FOREIGN KEY (FK_DEPARTURE) REFERENCES DEPARTURE,
	FOREIGN KEY (FK_DESTINATION) REFERENCES DESTINATION,
	UNIQUE (ID)
);

\p Creating table PASSENGER_FLIGHT
CREATE TABLE PASSENGER_FLIGHT (
    ID integer identity PRIMARY KEY,
	FK_PASSENGER integer,
	FK_FLIGHT integer,
    FOREIGN KEY (FK_PASSENGER) REFERENCES PASSENGER,
    FOREIGN KEY (FK_FLIGHT) REFERENCES FLIGHT,
	UNIQUE (ID)
);

